function Character(name, level, alignment, Sprites, Stats, Abilities, Inventory, Body, Loadout) {
	this.name = name;
	this.level = level;
	this.alignment = alignment;
	this.Sprites = Sprites;
	this.Stats = Stats;
	this.Abilities = Abilities;
	this.Inventory = Inventory;
	this.Body = Body;
	this.Loadout = Loadout;
}

